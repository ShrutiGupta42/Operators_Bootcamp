#include<iostream>
#include<cmath>
using namespace std;

int main()
{
	float a, sq, sqr;
	cout<<"Enter the no.";
	cin>>a;
	sq=a*a;
	sqr=sqrt(a);
	cout<<"the square of the given no. is:";
	cout<<sq;
	cout<<"\n";
	cout<<"the square root of the given no. is:";
	cout<<sqr;
	
	return 0;
}
